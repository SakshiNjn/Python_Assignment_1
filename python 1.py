a = float(input("Enter the first number (a): "))
b = float(input("Enter the second number (b): "))

floor_div = a // b

exponential = a ** b

are_equal= a == b

print(f"Floor Division of a // b is : {floor_div}")
print(f"a power b is: {exponential}")
print(f"Are a and b equal? {are_equal}")
